let x = 1;

//Relation Operator
console.log(x > 0);
console.log(x >= 1);
console.log(x < 0);
console.log(x <= 1);

//Equality 
console.log(x === 1);
console.log(x !== 1);

//Strict Equality (Type + value)
console.log(1 === 1);
console.log('1' === 1);
//Lose Equality
console.log(1 == 1);
console.log('1' == 1);
console.log(true == 1);